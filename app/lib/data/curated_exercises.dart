List<String> curatedExerciseIdsForMuscleGroups(Iterable<String> groups) {
  final ids = <String>[];
  final seen = <String>{};
  for (final group in groups) {
    for (final id in curatedExerciseIdsByMuscleGroup[group] ?? const []) {
      if (seen.add(id)) ids.add(id);
    }
  }
  return ids;
}
